package article.model;

public class Writer {
	private String wrtier_id;
	private String writer_name;
	public Writer(String wrtier_id, String writer_name) {
		super();
		this.wrtier_id = wrtier_id;
		this.writer_name = writer_name;
	}
	public String getWrtier_id() {
		return wrtier_id;
	}
	public String getWriter_name() {
		return writer_name;
	}
	
	
}
